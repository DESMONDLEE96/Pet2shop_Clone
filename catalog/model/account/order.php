	public function updateSubOrderComplete($sub_order_id, $customer_id) {
		$order_status_id = 4; //4 is completed out

		//take out status_datetime to edit
		$sub_order = $this->db->query("SELECT so.*, o.customer_id FROM " . DB_PREFIX . "sub_order so LEFT JOIN " . DB_PREFIX . "order o ON so.order_id = o.order_id WHERE sub_order_id = '" . (int)$sub_order_id . "'");

		if (!empty($sub_order) && $customer_id == $sub_order->row['customer_id']) {
			$status_dt = json_decode($sub_order->row['status_datetime'], true);
			$dt = $this->getTime();
			$status_dt['completed_datetime'] = $dt->format('d-m-Y H:i:s');

			$this->db->query("UPDATE " . DB_PREFIX . "sub_order SET order_status_id = '" . (int)$order_status_id . "', status_datetime = '" . json_encode($status_dt) . "' WHERE sub_order_id = '" . (int)$sub_order_id . "'");

			$this->updateOrderComplete($sub_order->row['order_id']);
		}
	}

	//check all sub order completed den only update order
	public function updateOrderComplete($order_id) {
		$order_status_id = 4;

		$sub_order = $this->db->query("SELECT sub_order_id FROM ". DB_PREFIX . "sub_order WHERE order_status_id !='". $order_status_id ."' AND order_id ='". (int)$order_id ."'");

		if (empty($sub_order->row)) {
			$this->db->query("UPDATE " . DB_PREFIX . "order SET order_status_id = '" . (int)$order_status_id . "' WHERE order_id = '" . (int)$order_id . "'");
		}
	}

	public function getTime() {
		$timestamp = time();
		$dt = new DateTime("now", new DateTimeZone('Asia/Kuala_Lumpur'));
		$dt->setTimestamp($timestamp);

		return $dt;
	}
}